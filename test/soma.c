#include <stdio.h>

#define MAXN 500000

int main() {
    int arr[MAXN], sum[MAXN+1], k, x, ans=0;
    scanf ("%d %d", &k, &x); sum[0] = 0;
    for (int i=0;i<k;++i) {
        scanf ("%d", &arr[i]);
        if (arr[i] == x) ++ans;
        sum[i+1] = sum[i] + arr[i];
    }
    for (int i=1;i<=k;++i) {
        for (int j=i+1;j<=k;++j) {
            if (sum[j]-sum[i-1] == x) {
                ++ans;
            } else if (sum[j] - sum[i-1] > x){
                break;
            }
        }
    }
    printf ("%d\n", ans);
}
